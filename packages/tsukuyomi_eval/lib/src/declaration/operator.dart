import 'package:tsukuyomi_eval/src/property.dart';

List<DartClass> get $operator {
  return [
    DartClass.operator(
      ($) => [
        $.unary('#!', ($) => $!),
        $.unary('-#', ($) => -$),
        $.unary('!#', ($) => !$),
        $.unary('~#', ($) => ~$),
        $.binary('+', ($, $$) => $ + $$),
        $.binary('-', ($, $$) => $ - $$),
        $.binary('*', ($, $$) => $ * $$),
        $.binary('/', ($, $$) => $ / $$),
        $.binary('%', ($, $$) => $ % $$),
        $.binary('~/', ($, $$) => $ ~/ $$),
        $.binary('>', ($, $$) => $ > $$),
        $.binary('<', ($, $$) => $ < $$),
        $.binary('>=', ($, $$) => $ >= $$),
        $.binary('<=', ($, $$) => $ <= $$),
        $.binary('==', ($, $$) => $ == $$),
        $.binary('!=', ($, $$) => $ != $$),
        $.binary('&', ($, $$) => $ & $$),
        $.binary('^', ($, $$) => $ ^ $$),
        $.binary('|', ($, $$) => $ | $$),
        $.binary('<<', ($, $$) => $ << $$),
        $.binary('>>', ($, $$) => $ >> $$),
        $.binary('>>>', ($, $$) => $ >>> $$),
        $.binary('[]', ($, $$) => $[$$]),
        $.ternary('[]=', ($, $$, $$$) => $[$$] = $$$),
      ],
    ),
  ];
}
